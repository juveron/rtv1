#include "libft.h"

void	ft_putnbr(int nb)
{
	int u;
	int flag;

	flag = 0;
	u = 1000000000;
	if (nb <= 0)
		ft_putchar(nb == 0 ? '0' : '-');
	while (u > 0)
	{
		if (flag == 1 || (nb / u) != 0)
		{
			ft_putchar(48 + (nb / u) * (nb < 0 ? -1 : 1));
			flag = 1;
			nb = nb % u;
		}
		u = u / 10;
	}
}
